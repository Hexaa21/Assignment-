public class While {
    public static void main(String[] args) {
        int n = 2;

        while( n < 10 ) {
        System.out.println("Aku Sayang Kamu");
        if ( n == 5 ){
            System.out.println("Aku Benci Kamu");
        } else {
            System.out.println("Tolong Kembali");
        }
        n++;
        }
    }
}